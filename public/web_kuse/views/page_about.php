		<h1 class="title">BEGINNING</h1>

		<p>The latest environmental design products by the Re-Leaf Studio, which has developed the environmental conscious products continuously since 2005.  Our studio’s principal has emphasized on using the waste from natural and daily life mixed with our creative mind. We had developed variety of products out of paper from banana pulp and fiber. The products were developed carefully from growing banana trees on the high land area, turn them into high quality materials then manufactured the environmentally friendly products with suchmaterials.</p>

		<p style="font-size:20px;font-weight:bold;margin:50px 0;">"Our studio’s principal has emphasized on using the waste from natural <br>
		and daily life mixed with our creative mind."</p>

		<p>Rubber Killer is moving forward to other steps, by looking for abandoned materials with tremendous hidden value like the car inner tubes. Our studio uses used inner tubes to develop new products such as messenger bags, Tote bags, backpacks, wallets, straps and ankle straps for cycling, etc..</p>

		<p>Rubber Killer uses the inner tubes from all types of vehicles, from the smallest inner tubes of bicycle to the inner tubes of eighteen-wheel trucks and tractors, which were normally abandoned, wasted with no benefit to the world.  The numbers of rubber inner tubes wasted are a huge burden on Mother Nature as the agricultural and developing countries continue using lots of tires each year. </p>

		<p>Saroengrong Wong-Savun , an independent architect and founder of the Re-Leaf Studio expects the Rubber Killer’s products to help alleviate this problem via his creativity and developing with an eco-friendly in mind. He combines his design and experience from his previous works under the idea “This world isunited”.All production processes are more cautious on their impact to our environment.</p>

